/**
 * GTFS形式のcalendar.txtとcalendar_dates.txtのデータに基づき、
 * 特定の運行日に有効なservice_idのリストを判定する関数。
 *
 * @param {string} targetDate - 判定したい日付 ('YYYYMMDD'形式、例: '20250805')。
 * @param {string} calendarData - calendar.txtの全内容（CSV形式）。
 * @param {string} calendarDatesData - calendar_dates.txtの全内容（CSV形式）。
 * @returns {string[]} 該当日付に有効なservice_idの配列。
 */
async function getActiveServiceIds(targetDate) {
    // calendar.txtとcalendar_dates.txtのデータを引数として受け取る
    const calendarData = calendar_txt;
    const calendarDatesData = calendar_dates_txt;
    // 1. データをパースするヘルパー関数
    const parseCSV = (csv) => {
        const lines = csv.trim().split('\n');
        const headers = lines[0].split(',');
        return lines.slice(1).map(line => {
            const values = line.split(',');
            const obj = {};
            headers.forEach((header, i) => {
                obj[header.trim()] = values[i].trim();
            });
            return obj;
        });
    };

    // 2. 曜日を判定する
    const year = parseInt(targetDate.substring(0, 4));
    const month = parseInt(targetDate.substring(4, 6)) - 1; // 月は0-indexed
    const day = parseInt(targetDate.substring(6, 8));
    const dateObj = new Date(year, month, day);

    // DateオブジェクトのgetDay()は日曜日が0、月曜日が1、...土曜日が6
    const dayOfWeek = dateObj.getDay();

    // calendar.txtの曜日列名に対応する曜日インデックス
    const dayMap = {
        1: 'monday',
        2: 'tuesday',
        3: 'wednesday',
        4: 'thursday',
        5: 'friday',
        6: 'saturday',
        0: 'sunday'
    };
    const targetDayColumn = dayMap[dayOfWeek];

    // 3. calendar.txtから基本運行サービスを抽出
    const calendarEntries = parseCSV(calendarData);
    let activeServices = new Set();

    calendarEntries.forEach(entry => {
        const serviceId = entry.service_id;
        const startDate = entry.start_date;
        const endDate = entry.end_date;
        const runsOnDay = entry[targetDayColumn] === '1';

        // 運行期間内で、かつ該当の曜日に運行が設定されているか
        if (targetDate >= startDate && targetDate <= endDate && runsOnDay) {
            activeServices.add(serviceId);
        }
    });

    // 4. calendar_dates.txtから例外を適用する
    const calendarDatesEntries = parseCSV(calendarDatesData);

    calendarDatesEntries.forEach(entry => {
        if (entry.date === targetDate) {
            const serviceId = entry.service_id;
            const exceptionType = entry.exception_type;

            if (exceptionType === '1') {
                // exception_type: 1 は「サービスを追加」
                activeServices.add(serviceId);
            } else if (exceptionType === '2') {
                // exception_type: 2 は「サービスを除外」
                activeServices.delete(serviceId);
            }
        }
    });

    // 5. 結果をソートして返す
    return Array.from(activeServices).sort();
}

